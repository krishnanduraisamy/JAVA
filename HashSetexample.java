package com.collections;

import java.util.HashSet;

public class  HashSetexample {

	public static void main(String[] args) {


		HashSet<Integer> s = new HashSet<>();
		
		
		s.add(101);
		s.add(10);
		s.add(201);
		s.add(null);
		s.add(null);
		
		System.out.println("with Hashset : "+s);
		

		System.out.println();

	}

}
  